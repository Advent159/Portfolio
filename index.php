    <script>
        document.addEventListener('DOMContentLoaded', function() {
            // Create particles
            const particlesContainer = document.getElementById('particles');
            const particleCount = 60;
            
            for (let i = 0; i < particleCount; i++) {
                const particle = document.createElement('div');
                particle.classList.add('particle');
                
                // Random properties
                const size = Math.random() * 6 + 2;
                const posX = Math.random() * 100;
                const delay = Math.random() * 5;
                const duration = Math.random() * 3 + 3;
                
                particle.style.width = `${size}px`;
                particle.style.height = `${size}px`;
                particle.style.left = `${posX}vw`;
                particle.style.animationDelay = `${delay}s`;
                particle.style.animationDuration = `${duration}s`;
                
                // Random color from gradient
                const colors = ['#667eea', '#764ba2', '#a78bfa', '#f093fb', '#f5576c'];
                particle.style.background = colors[Math.floor(Math.random() * colors.length)];
                
                particlesContainer.appendChild(particle);
            }
            
            // Animate text letters
            const creativeText = document.getElementById('creative-text');
            const text = creativeText.textContent;
            creativeText.textContent = '';
            
            // Pisahkan teks per kata, bukan per huruf
            const words = text.split(' ');
            
            for (let i = 0; i < words.length; i++) {
                const wordSpan = document.createElement('span');
                // Tambahkan spasi setelah kata kecuali kata terakhir
                wordSpan.textContent = words[i] + (i < words.length - 1 ? ' ' : '');
                wordSpan.style.animation = `letterReveal 0.5s forwards ${2 + i * 0.1}s`;
                creativeText.appendChild(wordSpan);
            }
            
            // Redirect after animation completes
            setTimeout(function() {
                // Tambahkan efek fade out pada container
                document.querySelector('.transition-container').style.opacity = '0';
                document.querySelector('.transition-container').style.transition = 'opacity 0.8s ease';
                
                // Redirect setelah fade out selesai
                setTimeout(function() {
                    window.location.href = "portfolio.html";
                }, 800);
            }, 5500);
        });
    </script>
